function display() {
    document.getElementById('our_clients').style.display="flex"
    document.getElementById('logo__btn').style.display="block"
}

function hide () {
    document.getElementById('our_clients').style.display= "none"
    document.getElementById('hide').style.display="block"
}



function show() {
    document.getElementById('more').style.display="block"
    document.getElementById('we-do-button').style.display="none"
}

function minimize () {
    document.getElementById('more').style.display= "none"
    document.getElementById('we-do-button').style.display=""
}